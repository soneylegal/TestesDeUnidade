// Função
function multiplicar(a, b) {
    return a * b;
}

// Teste de unidade
console.assert(multiplicar(2, 3) === 6, "Erro: 2 * 3 deveria ser 6");
console.assert(multiplicar(0, 5) === 0, "Erro: 0 * 5 deveria ser 0");
console.assert(multiplicar(-2, 4) === -8, "Erro: -2 * 4 deveria ser -8");
